<h1>Ejercicio 2: MVC en JavaScript</h1>
<p>Imagina que estás desarrollando una aplicación de lista de tareas en JavaScript utilizando el patrón MVC. A continuación, se describen las responsabilidades de cada componente:<br></p>
<h3>Modelo (TareaModelo):</h3>
<p>Representa la estructura de una tarea, con atributos como título, descripción, fecha de creación, etc.<br></p>
<p>Se encarga de realizar operaciones relacionadas con las tareas, como agregar una nueva tarea, obtener todas las tareas, marcar una tarea como completada, etc.<br></p>
<h3>Vista (TareaVista):</h3>
<p>Muestra la interfaz de usuario para ver y manipular las tareas.<br></p>
<p>Muestra una lista de tareas con su título y estado de completado.<br></p>
<p>Permite agregar nuevas tareas, marcar tareas como completadas y eliminar tareas.<br></p>
<h3>Controlador (TareaControlador):</h3>
<p>Recibe las acciones del usuario provenientes de la vista, como agregar una tarea, marcar una tarea como completada, etc.<br></p>
<p>Interactúa con el modelo para realizar las operaciones correspondientes en las tareas.<br></p>
<p>Actualiza la vista para reflejar los cambios realizados en las tareas.<br></p>
<br>
<p>Tu tarea consiste en implementar las clases TareaModelo, TareaVista y TareaControlador utilizando el patrón MVC. Además, debes crear una página web que muestre la lista de tareas, permita agregar nuevas tareas, marcar tareas como completadas y eliminar tareas.</p>
